//añade los numeros 0 al 5 en orden ascendente al arrat usando un loop for

const arr = [];
for (let x = 1; x < 6; ++x) {
  arr.push(x);
}
console.log(arr);

// añade los mumeros impares del 1 al 9 usando loop for

const arr1 = [];
for (let x = 1; x < 10; x += 2) {
  arr1.push(x);
}
console.log(arr1);

//añade los numeros pares del 1 al 9 en orden descendente en el arrat usando loop for

const arr2 = [];
for (let x = 8; x > 0; x -= 2) {
  arr2.push(x);
}
console.log(arr2);
